class Solution{
	public:
	int NthRoot(int n, int m)
	{
	    // Code here.
	    long long start = 1;
	    
	    while(true){
    
            long long val = pow(start, n);
            
            if(val == m)    return start;
            
            if(val > m)     break;
            
            ++start;
	    }
	    
	    return -1;
	}  
};

